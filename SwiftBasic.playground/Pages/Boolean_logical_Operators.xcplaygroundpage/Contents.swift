//Logical AND (&&)
let isSwiftFun = true
let isCodingCool = true
print(isSwiftFun && isCodingCool)

//Logical OR (||)
let isSwiftEasy = false
let isXcodePowerful = true
print(isSwiftEasy || isXcodePowerful)

//Logical NOT (!)
let isBugFree = false
print(!isBugFree)

//Combining Boolean Operators
let age = 25
let hasID = true
let canEnterClub = (age >= 18) && hasID
print(canEnterClub)





